class Compiler:
    def compile(self, code):
        """Simula a compilação de código e retorna uma mensagem."""
        return f"Compilando o código: {code}"

    def greet(self, name):
        """Retorna uma saudação."""
        return f"Olá, {name}!"

    def farewell(self, name):
        """Retorna uma despedida."""
        return f"Adeus, {name}!"
